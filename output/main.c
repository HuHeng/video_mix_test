/* Prepare a dummy image. */
static void fill_yuv_image(AVFrame *pict, int frame_index,
                           int width, int height)
{
    int x, y, i;

    i = frame_index;

    /* Y */
    for (y = 0; y < height; y++)
        for (x = 0; x < width; x++)
            pict->data[0][y * pict->linesize[0] + x] = x + y + i * 3;

    /* Cb and Cr */
    for (y = 0; y < height / 2; y++) {
        for (x = 0; x < width / 2; x++) {
            pict->data[1][y * pict->linesize[1] + x] = 128 + y + i * 2;
            pict->data[2][y * pict->linesize[2] + x] = 64 + x + i * 5;
        }
    }
}

static AVFrame *get_video_frame(OutputStream *ost)
{
    AVCodecContext *c = ost->enc;

    /* check if we want to generate more frames */
    if (av_compare_ts(ost->next_pts, c->time_base,
                      STREAM_DURATION, (AVRational){ 1, 1 }) >= 0)
        return NULL;

    /* when we pass a frame to the encoder, it may keep a reference to it
     * internally; make sure we do not overwrite it here */
    if (av_frame_make_writable(ost->frame) < 0)
        exit(1);

    if (c->pix_fmt != AV_PIX_FMT_YUV420P) {
        /* as we only generate a YUV420P picture, we must convert it
         * to the codec pixel format if needed */
        if (!ost->sws_ctx) {
            ost->sws_ctx = sws_getContext(c->width, c->height,
                                          AV_PIX_FMT_YUV420P,
                                          c->width, c->height,
                                          c->pix_fmt,
                                          SCALE_FLAGS, NULL, NULL, NULL);
            if (!ost->sws_ctx) {
                fprintf(stderr,
                        "Could not initialize the conversion context\n");
                exit(1);
            }
        }
        fill_yuv_image(ost->tmp_frame, ost->next_pts, c->width, c->height);
        sws_scale(ost->sws_ctx,
                  (const uint8_t * const *)ost->tmp_frame->data, ost->tmp_frame->linesize,
                  0, c->height, ost->frame->data, ost->frame->linesize);
    } else {
        fill_yuv_image(ost->frame, ost->next_pts, c->width, c->height);
    }

    ost->frame->pts = ost->next_pts++;

    return ost->frame;
}

//drive demux and decode, get a frame from the buffersink of filtergraph
//return value,  0 in case of continue, >0 got frame, <0 eof
AVPacket g_packet; //packet should be in a context pass to the get_video_frame_from_filter
int pendding = 0;//0 read a new packet
static int get_picture_from_decoder(AVFrame* frame){
    int ret;
    if(!pendding){
        av_init_packet(&g_packet);
        ret = av_read_frame(input_fmt_ctx, &g_packet);
        if(ret < 0 || g_packet.stream_index != video_stream_index){
            //error or end of file
            return ret;
        }
    }
    int got_frame = 0;
    ret = avcodec_decode_video2(dec_ctx, frame, &got_frame, &g_packet);
    if(ret < 0)
        return ret;
    g_packet.data += ret;
    g_packet.size -= ret;
    if(g_packet.size <= 0)
        pendding = 0;
    else
        pendding = 1;
    if(got_frame){
        return 1;
    }else{
        return 0;
    }
}
int get_one_picture_from_filter(AVFrame* frame)
{
    int got_picture = 0;
    while(got_picture == 0){
        got_picture = get_picture_from_decoder(frame);
    }
    return got_picture < 0 ? 0:1;
}

int input_valid = 1;

static AVFrame *get_one_video_frame(OutputStream *ost)
{
    AVCodecContext *c = ost->enc;
    int got_frame = 0;
    AVFrame* dummy_frame;
    AVFrame* output_frame;
    /* check if we want to generate more frames */
    if (av_compare_ts(ost->next_pts, c->time_base,
                      STREAM_DURATION, (AVRational){ 1, 1 }) >= 0)
        return NULL;

    /* when we pass a frame to the encoder, it may keep a reference to it
     * internally; make sure we do not overwrite it here */
    if (av_frame_make_writable(ost->frame) < 0)
        exit(1);
    if (av_frame_make_writable(ost->tmp_frame) < 0)
        exit(1);
    if(input_valid)
        input_valid = get_one_picture_from_filter(ost->filter_frame);
    int pic_format = ost->filter_frame->format;
    output_frame = ost->filter_frame;
    if(!input_valid){
        //restart
        //input_valid = get_one_picture_from_filter(ost->frame);
        fill_yuv_image(ost->tmp_frame, ost->next_pts, c->width, c->height);
        pic_format = AV_PIX_FMT_YUV420P;
        output_frame = ost->tmp_frame;
    }

    if (c->pix_fmt != pic_format) {
        /* as we only generate a YUV420P picture, we must convert it
         * to the codec pixel format if needed */
        if (!ost->sws_ctx) {
            ost->sws_ctx = sws_getContext(output_frame->width, output_frame->height,
                                          pic_format,
                                          c->width, c->height,
                                          c->pix_fmt,
                                          SCALE_FLAGS, NULL, NULL, NULL);
            if (!ost->sws_ctx) {
                fprintf(stderr,
                        "Could not initialize the conversion context\n");
                exit(1);
            }
        }
    //    fill_yuv_image(ost->tmp_frame, ost->next_pts, c->width, c->height);
        sws_scale(ost->sws_ctx,
                  (const uint8_t * const *)output_frame->data, output_frame->linesize,
                  0, c->height, ost->frame->data, ost->frame->linesize);
    } else {
      //  fill_yuv_image(ost->frame, ost->next_pts, c->width, c->height);
        ost->frame = output_frame;
    }

    ost->frame->pts = ost->next_pts++;

    return ost->frame;
}

/*
 * encode one video frame and send it to the muxer
 * return 1 when encoding is finished, 0 otherwise
 */
static int write_video_frame(AVFormatContext *oc, OutputStream *ost)
{
    int ret;
    AVCodecContext *c;
    AVFrame *frame;
    int got_packet = 0;
    AVPacket pkt = { 0 };

    c = ost->enc;

    frame = get_one_video_frame(ost);
    //frame = get_video_frame(ost);

    av_init_packet(&pkt);

    /* encode the image */
    ret = avcodec_encode_video2(c, &pkt, frame, &got_packet);
    if (ret < 0) {
        fprintf(stderr, "Error encoding video frame: %s\n", av_err2str(ret));
        exit(1);
    }

    if (got_packet) {
        ret = write_frame(oc, &c->time_base, ost, &pkt);
    } else {
        ret = 0;
    }

    if (ret < 0) {
        fprintf(stderr, "Error while writing video frame: %s\n", av_err2str(ret));
        exit(1);
    }

    return (frame || got_packet) ? 0 : 1;
}

static void close_stream(AVFormatContext *oc, OutputStream *ost)
{
    avcodec_free_context(&ost->enc);
    av_frame_free(&ost->frame);
    av_frame_free(&ost->tmp_frame);
    sws_freeContext(ost->sws_ctx);
    swr_free(&ost->swr_ctx);
}


